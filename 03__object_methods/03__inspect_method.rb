# Notes:
#   * .inspect - converts an object to a string representation that is fit to debugging
#   * Available for ALL objects

puts "Hello world\n" #=> nil
# OUTPUT:
#   Hello world

puts "Hello world\n".inspect #=> nil
# OUTPUT:
#   "Hello world\n"

p "Hello world\n" # => "Hello world\n"
# OUTPUT:
#   "Hello world\n"
