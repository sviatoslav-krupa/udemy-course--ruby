# Notes:
#   * Every expression in Ruby returns something (return is the final OUTPUT:, result)
#   * nil represents nothing, emptiness or blankness, absence of a value

puts 5 + 2 #=> nil
# OUTPUT:
#   7

print nil #=> nil
# OUTPUT: (outputs nothing)

puts nil #=> nil
# OUTPUT: (outputs only line break)
#

p nil #=> nil
# OUTPUT:
#   nil
