# Assignment Operators

# Simple assignment
a = 10
p a # => 10

a = a + 5 # => a = 10 + 5  => a = 15 (right side evaluates first)
p a # => 15

# Shorthands:

a = a + 5
a += 5 # shorthand

a = a - 3
a -= 3 # shorthand

a = a * 2
a *= 2 # shorthand

a = a / 3
a /= 3 # shorthand

a = a ** 3
a **= 3 # shorthand

a = a % 6
a %= 6 # shorthand
