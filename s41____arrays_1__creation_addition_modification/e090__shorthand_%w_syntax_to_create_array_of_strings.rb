# Shorthand %w Syntax to Create an Array of Strings
# Here (https://www.udemy.com/course/learn-to-code-with-ruby-lang/learn/lecture/6482082#overview) is the link to the video.

# the space is a beginning of a new string, new array element
names = %w[Jack Jill John James Sviatoslav Krupa]
p names # => ["Jack", "Jill", "John", "James", "Sviatoslav", "Krupa"]
