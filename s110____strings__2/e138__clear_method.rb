# The .clear Method on a String
# Here (https://www.udemy.com/course/learn-to-code-with-ruby-lang/learn/lecture/6482396#overview) is the link to the video.

# .clear - clears a string content and makes an empty string
# It mutates original variable

word = "goodbye"
p word # => "goodbye"

word.clear # => ""
p word # => ""
