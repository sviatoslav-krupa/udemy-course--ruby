# What is an array:
# * Ordered collection of objects (as a list).
# * Can contain objects with different types.
# * Itself is an object.
# * It acts as a container for other objects.
# * An array is created with a pair of square brackets `[]`.

p [] # => [] (empty array)
p [].class # => Array

